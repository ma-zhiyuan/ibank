package org.lanqiao.bank.base.entity;

/**
 * @ClassName: Admin
 * @Description: 管理员实体类
 * @author 西安工业大学蓝桥学员-谢世旺
 * @time 2017年4月22日 上午9:25:14
 */
public class Admin {
	private Integer id;
	private String name;
	private String phone;
	private String password;
	private String photo;
	private String email;

	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}

	/**
	 * @param phone
	 *            the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}

	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}

	/**
	 * @param password
	 *            the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}

	/**
	 * @return the photo
	 */
	public String getPhoto() {
		return photo;
	}

	/**
	 * @param photo
	 *            the photo to set
	 */
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	
	

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * @param name
	 * @param phone
	 * @param passsword
	 * @param photo
	 */
	public Admin(String name, String phone, String passsword, String photo, String email) {
		super();
		this.name = name;
		this.phone = phone;
		this.password = passsword;
		this.photo = photo;
		this.email = email;
	}

	/**
	 * 
	 */
	public Admin() {
		super();
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Admin [id=" + id + ", name=" + name + ", phone=" + phone + ", password=" + password + ", photo=" + photo
				+ "email" + email + "]";
	}
	
	

}
